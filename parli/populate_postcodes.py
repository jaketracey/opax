def populate(db=None):
    """Insert all postcode-to-electorate mappings into the database."""
    if db is None:
        db = get_db()

    mappings = build_postcode_map()

    # Deduplicate: count how many electorates each postcode maps to
    from collections import Counter
    pc_counts = Counter(pc for pc, _, _ in mappings)

    # Delete existing non-QLD data to avoid duplicates, keep QLD as-is
    db.execute("DELETE FROM postcode_electorates WHERE state != 'qld'")

    inserted = 0
    for postcode, electorate, state in mappings:
        ratio = 1.0 / pc_counts[postcode] if pc_counts[postcode] > 1 else 1.0
        try:
            db.execute(
                "INSERT OR REPLACE INTO postcode_electorates (postcode, electorate_name, state, ratio) VALUES (?, ?, ?, ?)",
                (postcode, electorate, state, round(ratio, 4)),
            )
            inserted += 1
        except Exception as e:
            print(f"  Error inserting {postcode} -> {electorate}: {e}")

    db.commit()
    print(f"Inserted {inserted} postcode-electorate mappings")

    # Summary
    states = db.execute(
        "SELECT state, COUNT(DISTINCT postcode) as postcodes, COUNT(DISTINCT electorate_name) as electorates FROM postcode_electorates GROUP BY state"
    ).fetchall()
    for s in states:
        print(f"  {s['state']}: {s['postcodes']} postcodes, {s['electorates']} electorates")

    return inserted


if __name__ == "__main__":
    populate()
